print(5 < 6) # the condition is True because 5 is less than 6 so it prints True value
print(5 == 5) # the condition is True because 5 is equal to 5 so it prints True value
print(5 != 6) # the condition is True because 5 is not equal to 6 so it prints True value
print(5 > 6) # the condition is False because 5 is greater than 6 so it prints False value